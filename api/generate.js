/* ── 선택 주제가 지원사업/정책자금 축인지 판정 ── */
function isPolicyAxis(p) {
  const axis = String(p.problemAxis || '').replace(/\s+/g, '');
  if (/지원사업|정책자금/.test(axis)) return true;
  const cat = String(p.subjectCategory || '');
  if (/지원·정책형|지원정책형/.test(cat)) return true;
  const t = String(p.subjectTitle || '');
  return /(지원사업|정책자금|지원금|바우처|보조금|소상공인\s?지원)/.test(t);
}

/* ── 선택 주제가 가산세/신고 후 결과 축인지 판정 ── */
function isPenaltyAxis(p) {
  const t = `${p.subjectTitle || ''} ${p.problemAxis || ''} ${p.subjectCategory || ''}`;
  return /(가산세|고지서|납부.*지연|신고.*세금이?\s?더|신고.*가산세)/.test(t);
}

