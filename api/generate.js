/* ── 주제 후보 후처리 — archive/rejected/basic 제거 + seed bank fallback ──
   감점이 아니라 "최종 후보에서 제거"한다. 후보가 5개에 못 미치면 유사 후보를 살리는 게 아니라
   CONCRETE_EPISODE_SEED_BANK에서 보강한다. 어쩔 수 없을 때만 soft pool을 마지막 수단으로 사용. */
function processSubjects(rawSubjects, payload) {
  const subjects = Array.isArray(rawSubjects) ? rawSubjects.slice() : [];
  const regenCtx = (payload && payload.regenerationContext) || {};
  const regenerateCount = Number(regenCtx.regenerateCount) || 0;
  const archiveItems = Array.isArray(payload && payload.archive) ? payload.archive : [];

  const excludeMap = buildEpisodeExcludeMap({
    archiveItems,
    rejectedSubjects:    Array.isArray(regenCtx.rejectedSubjects)    ? regenCtx.rejectedSubjects    : [],
    rejectedEpisodeKeys: Array.isArray(regenCtx.rejectedEpisodeKeys) ? regenCtx.rejectedEpisodeKeys : [],
  });

  const toScore = (s) => {
    const v = parseFloat(s && s.final_score);
    return isFinite(v) ? v : 0;
  };

  /* 1) 모든 후보에 보조 필드 계산 */
  subjects.forEach(s => {
    s._epKey         = normalizeEpisodeKey(s);
    s._effectiveAxis = effectiveAxis(s);
    s._basicPattern  = detectBasicOwnerAnxiety(s);
    s._broadGeneric  = detectBroadGenericTitle(s);
  });

  /* 2) final_score 내림차순으로 제거 사유 태깅 */
  const ranked = subjects.slice().sort((a, b) => toScore(b) - toScore(a));
  const seenInBatch = new Set();
  const policyAxisName = '지원사업·정책자금';
  let policyAxisCount = 0;
  ranked.forEach(s => {
    const k = s._epKey;
    const reasons = [];
    if (k && seenInBatch.has(k)) reasons.push('same-batch-duplicate:' + k);
    else if (k) seenInBatch.add(k);

    if (k && excludeMap.hardKeys.has(k) && excludeMap.map.get(k) && excludeMap.map.get(k).source === 'archive') {
      reasons.push('archive-hard:' + k);
    }
    if (k && excludeMap.rejectedKeys.has(k)) {
      reasons.push('rejected:' + k);
    }
    if (k && excludeMap.strongKeys.has(k) && !excludeMap.rejectedKeys.has(k)) {
      reasons.push('archive-strong:' + k);
    }
    if (s._effectiveAxis === policyAxisName) {
      policyAxisCount++;
      if (policyAxisCount >= 2) reasons.push('policy-axis-cap');
    }
    const eds = parseFloat(s.episode_diversity_score);
    if (isFinite(eds) && eds <= 2) reasons.push('episode-diversity≤2');

    if (regenerateCount >= 2) {
      if (s._basicPattern) reasons.push('basic-owner-anxiety:' + s._basicPattern);
      if (s._broadGeneric) reasons.push('broad-generic-title');
    }
    s._removeReasons = reasons;
  });

  /* 3) hard 제거 / soft 제거 분류
        hard: rejected, archive-hard, same-batch-duplicate, episode-diversity≤2,
              basic-owner-anxiety (regen≥2), broad-generic-title (regen≥2)
        soft: archive-strong, policy-axis-cap */
  const HARD_PREFIXES = ['rejected:', 'archive-hard:', 'same-batch-duplicate:', 'episode-diversity≤2', 'basic-owner-anxiety:', 'broad-generic-title'];
  const isHardRemoval = (reasons) => reasons.some(r => HARD_PREFIXES.some(p => r.startsWith(p)));
  const isAnyRemoval  = (reasons) => reasons.length > 0;

  /* "similar" = archive-strong reason (과거 아카이브와 구조가 비슷한 후보)
     "nonSimilar soft" = soft 제거 사유 중 archive-strong이 없는 경우 (policy-axis-cap 등)
     similar 후보는 finalSubjects 안에서 최대 1개(일반) ~ 2개(극단)까지만 허용. */
  const isSimilarReason = (reasons) => reasons.some(r => r.startsWith('archive-strong:'));
  const keepers = ranked.filter(s => !isAnyRemoval(s._removeReasons));
  const nonSimilarSoftPool = ranked.filter(s =>
    isAnyRemoval(s._removeReasons) && !isHardRemoval(s._removeReasons) && !isSimilarReason(s._removeReasons));
  const similarSoftPool = ranked.filter(s =>
    isAnyRemoval(s._removeReasons) && !isHardRemoval(s._removeReasons) && isSimilarReason(s._removeReasons));
  const blockedPool = ranked.filter(s => isHardRemoval(s._removeReasons));

  const finalSort = (a, b) => {
    const sa = toScore(a), sb = toScore(b);
    if (sa !== sb) return sb - sa;
    const da = parseFloat(a.episode_diversity_score) || 0;
    const db = parseFloat(b.episode_diversity_score) || 0;
    return db - da;
  };
  keepers.sort(finalSort);
  nonSimilarSoftPool.sort(finalSort);
  similarSoftPool.sort(finalSort);

  /* enriched seed 후보 pool — usedKeys/excludeMap 기준으로 선별, epKey 중복 없이 고른다. */
  const buildSeedPool = (usedKeys) =>
    CONCRETE_EPISODE_SEED_BANK.map(seed => {
      const e = Object.assign({}, seed);
      e._epKey         = normalizeEpisodeKey(e);
      e._effectiveAxis = effectiveAxis(e);
      e._basicPattern  = detectBasicOwnerAnxiety(e);
      e._broadGeneric  = detectBroadGenericTitle(e);
      e._seedFallback  = true;
      if (!isFinite(parseFloat(e.final_score)))            e.final_score = 7.5;
      if (!isFinite(parseFloat(e.episode_diversity_score))) e.episode_diversity_score = 8;
      if (!e.duplicate_risk) e.duplicate_risk = '낮음';
      return e;
    }).filter(seed => {
      const k = seed._epKey;
      if (!k) return false;
      if (usedKeys.has(k)) return false;
      if (excludeMap.hardKeys.has(k))     return false;
      if (excludeMap.rejectedKeys.has(k)) return false;
      if (excludeMap.strongKeys.has(k))   return false;
      if (seed._basicPattern || seed._broadGeneric) return false;
      return true;
    });

  const SIMILAR_CAP_DEFAULT   = 1;  /* 일반 상황 최대 허용 */
  const SIMILAR_CAP_EMERGENCY = 2;  /* seed/non-similar 모두 소진된 극단 상황의 절대 상한 */

  let finalSubjects = [];
  let seedFallbackUsed         = 0;
  let nonSimilarSoftUsed       = 0;
  let similarFallbackUsed      = 0;
  let similarOverflowPrevented = 0;
  let emergencyFillUsed        = 0;
  const usedKeys = new Set();

  /* Phase 1: keepers (정상 후보) */
  for (const s of keepers) {
    if (finalSubjects.length >= 5) break;
    if (s._epKey && usedKeys.has(s._epKey)) continue;
    if (s._epKey) usedKeys.add(s._epKey);
    finalSubjects.push(s);
  }

  /* Phase 2: seed bank (구체 사건 seed) — 유사 후보보다 먼저 */
  if (finalSubjects.length < 5) {
    const seeds = buildSeedPool(usedKeys);
    const seedSeen = new Set();
    for (const seed of seeds) {
      if (finalSubjects.length >= 5) break;
      if (seedSeen.has(seed._epKey)) continue;
      seedSeen.add(seed._epKey);
      usedKeys.add(seed._epKey);
      finalSubjects.push(seed);
      seedFallbackUsed++;
    }
  }

  /* Phase 3: non-similar softPool (정책 축 캡 등) — 안내 배지 없음 */
  if (finalSubjects.length < 5) {
    for (const s of nonSimilarSoftPool) {
      if (finalSubjects.length >= 5) break;
      if (s._epKey && usedKeys.has(s._epKey)) continue;
      if (s._epKey) usedKeys.add(s._epKey);
      finalSubjects.push(s);
      nonSimilarSoftUsed++;
    }
  }

  /* Phase 4: similar fallback — 기본 cap 1개. seed/non-similar가 다 떨어진 뒤에만. */
  if (finalSubjects.length < 5 && similarFallbackUsed < SIMILAR_CAP_DEFAULT) {
    for (const s of similarSoftPool) {
      if (finalSubjects.length >= 5) break;
      if (similarFallbackUsed >= SIMILAR_CAP_DEFAULT) break;
      if (s._epKey && usedKeys.has(s._epKey)) continue;
      if (s._epKey) usedKeys.add(s._epKey);
      s._similarFallback = true;
      finalSubjects.push(s);
      similarFallbackUsed++;
    }
  }

  /* Phase 5: emergency — 2번째 similar (총 2개) + console.warn. seed/non-similar/similar1개 다 소진 후. */
  if (finalSubjects.length < 5 && similarFallbackUsed < SIMILAR_CAP_EMERGENCY) {
    let usedEmergency = false;
    for (const s of similarSoftPool) {
      if (finalSubjects.length >= 5) break;
      if (similarFallbackUsed >= SIMILAR_CAP_EMERGENCY) {
        similarOverflowPrevented++;
        break;
      }
      if (s._epKey && usedKeys.has(s._epKey)) continue;
      if (s._epKey) usedKeys.add(s._epKey);
      s._similarFallback = true;
      finalSubjects.push(s);
      similarFallbackUsed++;
      usedEmergency = true;
    }
    if (usedEmergency) {
      console.warn('[subjects:filter] similarFallback 2개 사용 — seed/non-similar 모두 소진된 비정상 상황');
    }
  }

  /* Phase 6 — 5개 미달 시 seed bank lenient 우선 보강 (archive-strong까지 허용).
     "blocked pool(basic/duplicate) 보다 seed bank 후보가 화면에서 더 낫다"는 원칙. */
  if (finalSubjects.length < 5) {
    const lenientSeeds = CONCRETE_EPISODE_SEED_BANK.map(seed => {
      const e = Object.assign({}, seed);
      e._epKey         = normalizeEpisodeKey(e);
      e._effectiveAxis = effectiveAxis(e);
      e._basicPattern  = detectBasicOwnerAnxiety(e);
      e._broadGeneric  = detectBroadGenericTitle(e);
      e._seedFallback  = true;
      if (!isFinite(parseFloat(e.final_score)))             e.final_score = 7.0;
      if (!isFinite(parseFloat(e.episode_diversity_score))) e.episode_diversity_score = 8;
      if (!e.duplicate_risk) e.duplicate_risk = '낮음';
      return e;
    }).filter(seed => {
      const k = seed._epKey;
      if (!k) return false;
      if (usedKeys.has(k)) return false;
      if (excludeMap.hardKeys.has(k))     return false;
      if (excludeMap.rejectedKeys.has(k)) return false;
      return true;  /* archive-strong 허용 */
    });
    for (const seed of lenientSeeds) {
      if (finalSubjects.length >= 5) break;
      if (usedKeys.has(seed._epKey)) continue;
      usedKeys.add(seed._epKey);
      finalSubjects.push(seed);
      seedFallbackUsed++;
    }
  }

  /* Phase 7 — 그래도 부족하면 blocked pool 동원 (basic/broad/duplicate).
     이들은 "유사(archive-strong)"가 아니므로 _similarFallback이 아닌 _lastResort로만 표시. */
  if (finalSubjects.length < 5) {
    for (const s of blockedPool) {
      if (finalSubjects.length >= 5) break;
      if (s._epKey && usedKeys.has(s._epKey)) continue;
      if (s._epKey) usedKeys.add(s._epKey);
      s._lastResort = true;
      finalSubjects.push(s);
      emergencyFillUsed++;
    }
  }

  /* Phase 8 — 절대 보장. seed bank 전체 동원 (hard/rejected까지). usedKeys만 회피. */
  if (finalSubjects.length < 5) {
    console.warn('[subjects:filter] 5개 보장 desperate phase 진입 — current=' + finalSubjects.length);
    const desperateSeeds = CONCRETE_EPISODE_SEED_BANK.map(seed => {
      const e = Object.assign({}, seed);
      e._epKey         = normalizeEpisodeKey(e);
      e._effectiveAxis = effectiveAxis(e);
      e._seedFallback  = true;
      if (!isFinite(parseFloat(e.final_score)))             e.final_score = 6.5;
      if (!isFinite(parseFloat(e.episode_diversity_score))) e.episode_diversity_score = 7;
      if (!e.duplicate_risk) e.duplicate_risk = '낮음';
      return e;
    }).filter(seed => seed._epKey && !usedKeys.has(seed._epKey));
    for (const seed of desperateSeeds) {
      if (finalSubjects.length >= 5) break;
      usedKeys.add(seed._epKey);
      finalSubjects.push(seed);
      seedFallbackUsed++;
      emergencyFillUsed++;
    }
  }

  /* 안전망: 어떤 경로로든 finalSubjects 안에 similar cap을 초과한 후보가 들어가면 잘라낸다.
     단, finalSubjects.length가 이미 5 미만이면 잘라내지 않는다 — 5개 보장이 우선. */
  if (finalSubjects.length >= 5
      && finalSubjects.filter(s => s._similarFallback).length > SIMILAR_CAP_EMERGENCY) {
    let kept = 0;
    finalSubjects = finalSubjects.filter(s => {
      if (!s._similarFallback) return true;
      if (kept < SIMILAR_CAP_EMERGENCY) { kept++; return true; }
      similarOverflowPrevented++;
      console.warn('[subjects:filter] similarFallback cap 초과 후보 제거 — title=' + s.title);
      return false;
    });
    similarFallbackUsed = finalSubjects.filter(s => s._similarFallback).length;
  }

  if (finalSubjects.length < 5) {
    console.warn('[subjects:filter] 5개 보장 최종 실패 — finalSubjects.length=' + finalSubjects.length);
  }

  /* 7) 통계 */
  const stats = {
    generatedCount:            ranked.length,
    removedByArchiveHard:      ranked.filter(s => s._removeReasons.some(r => r.startsWith('archive-hard:'))).length,
    removedByArchiveStrong:    ranked.filter(s => s._removeReasons.some(r => r.startsWith('archive-strong:'))).length,
    removedByRejected:         ranked.filter(s => s._removeReasons.some(r => r.startsWith('rejected:'))).length,
    removedBySameBatchDup:     ranked.filter(s => s._removeReasons.some(r => r.startsWith('same-batch-duplicate:'))).length,
    removedByBasicPattern:     ranked.filter(s => s._removeReasons.some(r => r.startsWith('basic-owner-anxiety:'))).length,
    removedByBroadGeneric:     ranked.filter(s => s._removeReasons.includes('broad-generic-title')).length,
    removedByPolicyAxisCap:    ranked.filter(s => s._removeReasons.includes('policy-axis-cap')).length,
    seedFallbackUsed,
    nonSimilarSoftUsed,
    similarFallbackUsed,
    similarOverflowPrevented,
    emergencyFillUsed,
    finalCount: finalSubjects.length,
    regenerateCount,
  };

  return { finalSubjects, stats, excludeMap };
}

