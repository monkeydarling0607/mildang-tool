/** 제목·문장 토큰화 — 공통 헬퍼 */
function tokenize(s) {
  return String(s || '').replace(/[!?.,…\s]+/g, ' ').split(/\s+/).filter(t => t.length >= 2);
}

/* problem_axis 정규화 — 표기 차이를 흡수하기 위해 핵심 키워드로 정규화 */
function normalizeProblemAxis(raw) {
  const s = String(raw || '').replace(/\s+/g, '');
  if (!s) return '';
  if (/(카드|증빙|영수증|경비|적격증빙|세금계산서)/.test(s)) return '카드·증빙·경비';
  if (/(직원|급여|4대보험|원천|인건|알바|노무|프리랜서)/.test(s))   return '직원·급여·4대보험';
  if (/(지원|정책자금|바우처|보조금|융자)/.test(s))                  return '지원사업·정책자금';
  if (/(매출|정산|현금흐름|통장|고정비|플랫폼)/.test(s))             return '매출·정산·현금흐름';
  if (/(명의|주소|계약|임대|폐업|휴업|동업|양수도|등록)/.test(s))    return '사업자 정보·명의·주소·계약';
  return s;
}

/* problem_axis 필드 외에 title/summary/hook 텍스트도 봐서 실효 축을 산출.
   "지원금 언제 올까요?", "지원금이라도 챙겨야죠" 같이 축 필드는 다른데
   실제로는 지원사업 소재인 경우를 잡기 위함. */
function effectiveAxis(s) {
  const fromField = normalizeProblemAxis(s && s.problem_axis);
  if (fromField === '지원사업·정책자금') return fromField;
  const blob = `${(s && s.title) || ''} ${(s && s.summary) || ''} ${(s && s.hook) || ''} ${(s && s.topic_cluster) || ''}`.replace(/\s+/g, '');
  if (/(지원금|지원사업|정책자금|바우처|보조금|융자|소상공인지원|챙겨야죠|챙기세요|언제\s?받|언제\s?오나|신청\s?마감|공고|보조사업)/.test(blob)) {
    return '지원사업·정책자금';
  }
  if (fromField) return fromField;
  if (/(카드|증빙|영수증|경비|적격증빙|세금계산서)/.test(blob))     return '카드·증빙·경비';
  if (/(직원|급여|4대보험|원천|인건|알바|노무|프리랜서)/.test(blob)) return '직원·급여·4대보험';
  if (/(매출|정산|현금흐름|통장|고정비|플랫폼)/.test(blob))           return '매출·정산·현금흐름';
  if (/(명의|주소|계약|임대|폐업|휴업|동업|양수도|등록)/.test(blob))  return '사업자 정보·명의·주소·계약';
  return '';
}

/**
 * 주제 후보 배열 중복 검사
 * - 제목 키워드 2개 이상 겹치는 후보 쌍
 * - subject_category 중복
 * - topic_cluster 중복
 * - problem_axis 중복 (정규화된 5종 축)
 * - duplicate_risk "높음" 후보 개수
 */
function checkSubjectsDuplicate(subjects) {
  const warnings = [];
  if (!Array.isArray(subjects) || subjects.length < 2) return warnings;

  /* 1) duplicate_risk "높음" 검출 */
  const highRisk = subjects.filter(s => s.duplicate_risk === '높음').length;
  if (highRisk > 0) warnings.push(`duplicate_risk "높음" 주제 ${highRisk}개 — 배열 하단 배치`);

  /* 2) subject_category 중복 */
  const catMap = {};
  subjects.forEach(s => {
    const c = (s.subject_category || '').trim();
    if (c) catMap[c] = (catMap[c] || 0) + 1;
  });
  Object.entries(catMap).forEach(([c, n]) => {
    if (n >= 2) warnings.push(`카테고리 "${c}" ${n}회 중복`);
  });

  /* 3) topic_cluster 중복 */
  const clMap = {};
  subjects.forEach(s => {
    const raw = (s.topic_cluster || '').replace(/\s*—.*$/, '').trim();
    if (raw && raw !== '기타') clMap[raw] = (clMap[raw] || 0) + 1;
  });
  Object.entries(clMap).forEach(([c, n]) => {
    if (n >= 2) warnings.push(`주제군 "${c}" ${n}회 중복`);
  });

  /* 4) problem_axis 중복 (정규화 + 실효 축) — 5개가 모두 다른 축이어야 함
        지원사업·정책자금은 단 1개만 허용. 제목·요약에 지원금 소재가 보이면 자동 ③번 축으로 본다. */
  const axisMap = {};
  subjects.forEach(s => {
    const norm = effectiveAxis(s);
    if (!norm) return;
    axisMap[norm] = (axisMap[norm] || 0) + 1;
  });
  Object.entries(axisMap).forEach(([c, n]) => {
    if (n >= 2) warnings.push(`문제 축 "${c}" ${n}회 중복 — 표현만 다르고 같은 축. 다른 축으로 교체 권장`);
  });
  if (axisMap['지원사업·정책자금'] && axisMap['지원사업·정책자금'] >= 2) {
    warnings.push(`지원사업·정책자금 후보 ${axisMap['지원사업·정책자금']}개 — 후보 5개 중 최대 1개만 허용. 두 번째부터 자동 강등됨`);
  }

  /* 5) 제목 키워드 2개 이상 겹침 */
  for (let i = 0; i < subjects.length; i++) {
    for (let j = i + 1; j < subjects.length; j++) {
      const ti = tokenize(subjects[i].title);
      const tj = tokenize(subjects[j].title);
      const overlap = ti.filter(t => tj.includes(t));
      if (overlap.length >= 2) {
        warnings.push(`주제 #${i+1}/#${j+1} 제목 키워드 ${overlap.length}개 겹침`);
      }
    }
  }
  return warnings;
}

/**
 * 사연 후보 배열 중복 검사
 * - tone(유형) 2회 이상 반복
 * - industry(업종) 2회 이상 반복
 * - 첫 문장 시작 패턴 중복
 * - "신고했는데 가산세 고지서" 류 좁은 갈등 구조 반복
 */
function checkStoriesDuplicate(stories) {
  const warnings = [];
  if (!Array.isArray(stories) || stories.length < 2) return warnings;

  /* tone 중복 */
  const toneMap = {};
  stories.forEach(s => {
    const t = (s.tone || '').trim();
    if (t) toneMap[t] = (toneMap[t] || 0) + 1;
  });
  Object.entries(toneMap).forEach(([t, n]) => {
    if (n >= 2) warnings.push(`사연 유형 "${t}" ${n}회 반복`);
  });

  /* industry 중복 */
  const indMap = {};
  stories.forEach(s => {
    const k = String(s.industry || '').replace(/\s+/g,'').slice(0, 6);
    if (k) indMap[k] = (indMap[k] || 0) + 1;
  });
  Object.entries(indMap).forEach(([k, n]) => {
    if (n >= 2) warnings.push(`업종 "${k}" ${n}회 반복`);
  });

  /* 첫 문장 패턴 */
  const chunkMap = {};
  stories.forEach((s, i) => {
    const key = String(s.text || '').replace(/[\s.,!?…]+/g, '').slice(0, 6);
    if (!key) return;
    chunkMap[key] = chunkMap[key] || [];
    chunkMap[key].push(i + 1);
  });
  Object.entries(chunkMap).forEach(([k, idxList]) => {
    if (idxList.length >= 2) warnings.push(`첫 문장 시작 패턴 "${k}…" 반복 (#${idxList.join(', #')})`);
  });

  /* 좁은 갈등 구조 반복 */
  let narrowCount = 0;
  stories.forEach(s => {
    const t = String(s.text || '');
    if (/(가산세|신고했는데|신고했더니).*고지서/.test(t)) narrowCount++;
  });
  if (narrowCount >= 2) {
    warnings.push(`"신고-가산세 고지서" 갈등 구조 ${narrowCount}회 반복 — 다른 갈등 구조 권장`);
  }

  /* ── 27화 패턴 (신고 후 세금/가산세/고지서/매출 대비 세금 증가) ──
     27화 「신고 마치고 세금이 더 나왔어요, 도대체 왜죠?」와 구조가 같으면
     단 1건이라도 경고를 띄워서 즉시 교체를 유도한다. */
  const banned27Patterns = [
    { re: /신고.*(했는데|했더니|마치|마쳤|끝났|끝내).*(세금이? 더|세금까지|세금이 늘|가산세)/, label: '신고 후 세금/가산세 증가' },
    { re: /가산세.*(붙었|나왔|떴|고지서)/,                                                       label: '가산세 발생' },
    { re: /고지서.*(받|와서|보고).*(당황|놀랐|모르겠|왜)/,                                       label: '고지서 받고 당황' },
    { re: /(신고가 맞는지|신고 맞는지|뭐부터 (확인|봐야|챙겨))/,                                 label: '신고 맞는지/뭐부터 확인' },
    { re: /매출.*(안 늘었|그대로|줄었).*세금.*(더|늘었)/,                                        label: '매출 정체에 세금 증가' },
  ];
  banned27Patterns.forEach(p => {
    const hits = [];
    stories.forEach((s, i) => {
      const t = `${s.text || ''} ${s.title || ''} ${s.concern || ''} ${s.question || ''}`;
      if (p.re.test(t)) hits.push(i + 1);
    });
    if (hits.length > 0) {
      warnings.push(`27화 패턴 "${p.label}" 감지 (#${hits.join(', #')}) — 다른 갈등 구조로 교체 권장`);
    }
  });

  /* ── conflict_axis / problem_structure / tax_question_axis / resolution_axis 중복 검사 ──
     "업종만 바뀌고 같은 사연" 패턴을 잡아내는 핵심 로직.
     축 값이 서로 같으면 (대소문자·공백·구두점 무시) 같은 구조로 본다. */
  const normAxis = v => String(v || '').toLowerCase().replace(/[\s.,!?…·\-]+/g, '');
  const axisFields = [
    { key: 'conflict_axis',     label: '갈등 축' },
    { key: 'problem_structure', label: '문제 구조' },
    { key: 'tax_question_axis', label: '세무 질문 축' },
    { key: 'resolution_axis',   label: '해결 방향 축' },
  ];
  axisFields.forEach(f => {
    const map = {};
    stories.forEach((s, i) => {
      const k = normAxis(s[f.key]);
      if (!k) return;
      (map[k] = map[k] || []).push(i + 1);
    });
    Object.entries(map).forEach(([k, idxList]) => {
      if (idxList.length >= 2) {
        warnings.push(`${f.label}이(가) #${idxList.join(', #')} 사이에서 동일 — 대상만 바꾼 같은 구조로 보임. 1개만 남기고 교체 권장`);
      }
    });
  });

  /* ── 구조 가족(structural family) 검사 ──
     주체(사장님/직원/거래처/카드 내역/결산 시점)만 바뀐 같은 흐름은 한 가족으로 묶어
     2개 이상이면 1개만 남기도록 경고한다. */
  const structureFamilies = [
    {
      name: '증빙·영수증·경비 누락',
      re: /(영수증|증빙|적격증빙|세금계산서|경비)[가-힣\s,.…]{0,30}(누락|빠지|빠진|빼먹|놓치|안\s?챙|못\s?챙)/,
    },
    {
      name: '납부 지연·기한 놓침',
      re: /(납부|납기|기한|마감|만기)[가-힣\s,.…]{0,20}(지연|놓치|넘기|지났|지나)/,
    },
  ];
  structureFamilies.forEach(fam => {
    const hits = [];
    stories.forEach((s, i) => {
      const t = `${s.text || ''} ${s.title || ''} ${s.problem_structure || ''} ${s.conflict_axis || ''} ${s.concern || ''}`;
      if (fam.re.test(t)) hits.push(i + 1);
    });
    if (hits.length >= 2) {
      warnings.push(`"${fam.name}" 구조 ${hits.length}회 (#${hits.join(', #')}) — 같은 가족. 1개만 남기고 다른 conflict_axis로 교체`);
    }
  });

  return warnings;
}

/* ── 선택 주제 기반 사연 적합성 검증 ──
   주제 키워드 / 문제 축에 따라 어떤 업종·맥락이 필수인지를 판단해서
   "학원 원장이 매출 보고서를 늦게 봤다" 같은 맥락 어긋남 사연을 감지한다.
   결과는 경고로만 전달 (클라이언트가 배지/배너로 표시). */
function computeSubjectFitRulesServer(payload) {
  if (!payload) return null;
  const blob = `${payload.subjectTitle || ''} ${payload.subjectSummary || ''} ${payload.problemAxis || ''} ${payload.conflict || ''} ${payload.taxQuestion || ''}`;

  if (/(플랫폼|정산|입점|스마트스토어|쿠팡|배민|배달의민족|요기요|네이버\s?페이|오픈마켓|수수료|판매대금|정산금|판매\s?수수료|예약\s?플랫폼)/.test(blob)
      || (/(현금흐름|자금\s?부족|입금\s?지연|통장)/.test(blob) && /(매출|정산|판매)/.test(blob))) {
    return {
      label: '플랫폼 정산·현금흐름',
      mustIndustryRe: /(쇼핑몰|스마트스토어|쿠팡|오픈마켓|배달|배민|요기요|플랫폼|예약|네이버|온라인|입점|판매자|프리랜서|도소매)/,
      mustContextRe:  /(플랫폼|정산|수수료|입금|판매대금|판매\s?수수료|예약\s?플랫폼|광고비|배송비|재고|현금흐름|통장|입금\s?지연|정산\s?주기)/,
      excludeIndustryRe: /(학원|미용실|병원|의원|카페|음식점|제조|매장|오프라인)/,
      excludeContextRe:  /(매출\s?보고서|장부\s?확인|보고서.*늦게|보고서.*뒤늦)/,
    };
  }
  if (/(직원|급여|4대보험|원천징수|채용|알바|아르바이트|인건비|노무)/.test(blob)) {
    return {
      label: '직원·급여·4대보험',
      mustIndustryRe: /(음식점|카페|병원|의원|학원|제조|도소매|매장|미용실|쇼핑몰|회사|법인|사무실)/,
      mustContextRe:  /(직원|급여|4대보험|원천징수|채용|알바|아르바이트|인건비|노무|월급)/,
    };
  }
  if (/(지원사업|정책자금|보조금|바우처|융자|소상공인\s?지원|공고)/.test(blob)) {
    return {
      label: '지원사업·정책자금',
      mustContextRe: /(지원|정책|보조|바우처|융자|공고|신청|소상공인|창업|영세)/,
    };
  }
  if (/(카드\s?사용|적격증빙|세금계산서|영수증|경비\s?처리|증빙|현금영수증)/.test(blob)) {
    return {
      label: '카드·증빙·경비',
      mustIndustryRe: /(쇼핑몰|카페|음식점|미용실|프리랜서|도소매|학원|병원|온라인)/,
      mustContextRe:  /(카드|영수증|증빙|세금계산서|경비|적격증빙|현금영수증)/,
    };
  }
  return null;
}

function checkSubjectFitStories(stories, rule) {
  const warnings = [];
  const misfitIdx = [];
  if (!rule || !Array.isArray(stories)) return { warnings, misfitIdx };
  stories.forEach((s, i) => {
    const text = `${s.text || ''} ${s.title || ''} ${s.industry || ''} ${s.narrator_profile || ''} `
               + `${s.conflict_axis || ''} ${s.problem_structure || ''} ${s.concern || ''} ${s.question || ''}`;
    const industry = String(s.industry || '').replace(/\s+/g, '');
    let reason = '';
    if (rule.excludeIndustryRe && industry && rule.excludeIndustryRe.test(industry)
        && (!rule.mustContextRe || !rule.mustContextRe.test(text))) {
      reason = `주제와 맞지 않는 업종(${industry}) + 핵심 맥락 누락`;
    } else if (rule.excludeContextRe && rule.excludeContextRe.test(text)
        && (!rule.mustContextRe || !rule.mustContextRe.test(text))) {
      reason = '주제 핵심과 무관한 단순 확인·장부 지연 패턴';
    } else if (rule.mustIndustryRe && industry && !rule.mustIndustryRe.test(industry)
        && (!rule.mustContextRe || !rule.mustContextRe.test(text))) {
      reason = '주제에 적합한 업종 아님 + 핵심 맥락 누락';
    } else if (rule.mustContextRe && !rule.mustContextRe.test(text)) {
      reason = `주제 핵심 키워드(${rule.label})가 사연에 드러나지 않음`;
    }
    if (reason) {
      misfitIdx.push(i + 1);
      warnings.push(`사연 #${i + 1} 적합성 낮음: ${reason}`);
    }
  });
  return { warnings, misfitIdx };
}

/* ── caution 정규화 — 빈 값/"없음"/"해당 없음" 등은 기본 안내 문구로 대체 ── */
const EMPTY_CAUTION_VALUES = new Set(['없음', '해당 없음', 'n/a', 'na', '-', '']);
const DEFAULT_CAUTION_TEXT = '신청 전 공고문과 접수처의 세부 조건을 반드시 확인해주세요.';
function normalizeCaution(val) {
  const v = (val || '').trim().toLowerCase();
  return EMPTY_CAUTION_VALUES.has(v) ? DEFAULT_CAUTION_TEXT : (val || '').trim() || DEFAULT_CAUTION_TEXT;
}

