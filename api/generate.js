/** 텍스트 배열을 합쳐 해당하는 클러스터 이름 목록을 반환 */
function detectClusters(texts) {
  const joined = (Array.isArray(texts) ? texts : [texts])
    .filter(Boolean).join(' ');
  return TOPIC_CLUSTERS
    .filter(c => c.keys.some(k => joined.includes(k)))
    .map(c => c.name);
}

