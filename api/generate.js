/* ── 주제 축과 양립 불가한 꿀팁 패턴 (선택 주제가 가산세 축이 아닌 경우 차단) ── */
const TIP_INCOMPATIBLE_PATTERNS = [
  { re: /가산세/,                                                 label: '가산세 소재' },
  { re: /(신고|납부)[가-힣\s]{0,8}고지서/,                        label: '신고/납부 고지서 소재' },
  { re: /고지서[가-힣\s]{0,8}(받|왔|와서|당황|놀랐)/,             label: '고지서 받음 소재' },
  { re: /(납부|납기|기한|마감)[가-힣\s]{0,15}(지연|놓치|넘기|지났)/, label: '납부 지연 소재' },
  { re: /(영수증|증빙|적격증빙|세금계산서|경비)[가-힣\s,.…]{0,30}(누락|빠지|빠진|빼먹|놓치)/, label: '증빙·자료 누락 소재' },
];

/**
 * AI 생성 꿀팁 배열을 검사해서 유효/제외 분리 반환.
 * 제외 기준:
 *   1) 강제 금지 키워드 (BANNED_TIP_KEYWORDS)
 *   2) 선택 주제 축과 양립 불가한 구조 (지원사업 주제인데 가산세/고지서 등)
 * ※ source_url 없는 혜택형은 제거하지 않고 "공고 확인 필요" 상태로 유지.
 *   (원고 생성 전 클라이언트에서 별도 게이트 처리)
 * @param {Array} tips        AI 응답 tips 배열
 * @param {object} ctx        선택 주제 컨텍스트 (problemAxis, subjectTitle, subjectCategory)
 * @returns {{ valid: Array, removed: Array<{title,reason}> }}
 */
function filterTips(tips, ctx) {
  const valid   = [];
  const removed = [];
  const blockIncompatible = ctx ? (isPolicyAxis(ctx) || (!isPenaltyAxis(ctx) && (ctx.problemAxis || ctx.subjectCategory))) : false;

  for (const tip of (Array.isArray(tips) ? tips : [])) {
    const text = [
      tip.title, tip.benefit, tip.background, tip.target,
      tip.check_now, tip.caution, tip.search_keyword,
    ].filter(Boolean).join(' ');

    /* ── 강제 금지 키워드만 제거 ── */
    const banned = BANNED_TIP_KEYWORDS.find(kw => text.includes(kw));
    if (banned) {
      console.log('[tips-validate] removed:', tip.title?.slice(0,40), '| reason: 금지 키워드 —', banned);
      removed.push({ title: tip.title || '', reason: '금지 키워드: ' + banned });
      continue;
    }

    /* ── 주제 축 양립 불가 패턴 — 선택 주제가 가산세 축이 아닐 때만 ── */
    if (blockIncompatible) {
      const hit = TIP_INCOMPATIBLE_PATTERNS.find(p => p.re.test(text));
      if (hit) {
        const reason = (ctx && isPolicyAxis(ctx))
          ? `선택 주제(지원사업/정책자금)와 다른 축: ${hit.label}`
          : `선택 주제(${ctx && ctx.problemAxis ? ctx.problemAxis : ctx && ctx.subjectCategory || '주제'})와 다른 축: ${hit.label}`;
        console.log('[tips-validate] removed:', (tip.title || '').slice(0, 40), '| reason:', reason);
        removed.push({ title: tip.title || '', reason });
        continue;
      }
    }

    valid.push(tip);
  }

  return { valid, removed };
}

